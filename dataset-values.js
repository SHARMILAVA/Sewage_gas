// Track current index per tunnel for cycling through dataset points
const datasetIndex = {
    mining: 0,
    stormwater: 0,
    utility: 0,
    transportation: 0,
    construction: 0,
    storage: 0
};

// Get next dataset point for a tunnel (cycles through the 50 points with slight variation)
function getDatasetPoint(tunnelId) {
    const points = DATASET_VALUES[tunnelId];
    if (!points) return null;

    const idx = datasetIndex[tunnelId];
    const point = points[idx];

    // Advance index (cycle back to 0 after reaching end)
    datasetIndex[tunnelId] = (idx + 1) % points.length;

    // Add small realistic variation (±2%) to avoid perfectly static values
    const varied = {};
    for (const key in point) {
        const val = point[key];
        const variation = val * (0.98 + Math.random() * 0.04); // ±2%
        varied[key] = parseFloat(variation.toFixed(key === 'pressure' ? 2 : 1));
    }
    return varied;
}
